CREATE TABLE REPORT
(
REPORT_ID INT PRIMARY KEY IDENTITY(1, 1),
USERID INT,
FULL_NAME VARCHAR(20),
TEST_LEVEL INT,
TECHNOLOGY_ID INT,
DATE_OF_EXAM DATE,
SCORE INT,
EMAIL VARCHAR(30),
FOREIGN KEY(TECHNOLOGY_ID) REFERENCES TECHNOLOGY(TECHNOLOGY_ID),
FOREIGN KEY(USERID) REFERENCES USER_DETAIL(USERID)
)

